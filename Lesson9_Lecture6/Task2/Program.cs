void printMatrix(int[,] matrix)
{
    for (int r = 0; r < matrix.GetLength(0); r++)
    {
        for (int c = 0; c < matrix.GetLength(1); c++)
        {
            if (matrix[r,c] == 1)
                Console.Write("+");
            else
                Console.Write(" ");
        }   
        Console.WriteLine();
    }
}



